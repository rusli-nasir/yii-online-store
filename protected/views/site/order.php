<h2>Order Status</h2>
<h4>How can you check your order status.</h4>
<p>
    If you created an account with us, you can log in and click on Order Status & History to view all of your past orders. From there you can click on any order to obtain the detailed status of that order.
   </p>
   <p>
    If you did not create an account with us, after you click on the Order Status link, simply enter in your order number and your billing zip/postal code to view your order status.
</p>
